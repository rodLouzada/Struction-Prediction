
public class Cell2 {
	int value;
	int k;
	public Cell2(int value, int k) {
		super();
		this.value = value;
		this.k = k;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getK() {
		return k;
	}
	public void setK(int k) {
		this.k = k;
	}
	
}
